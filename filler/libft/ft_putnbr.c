#include "libft.h"

void	ft_putnbr(int c)
{
	int a;

	a = c % 10 + 48;
	if (c >= 10)
	{
		ft_putnbr(c / 10);
		write(1, &a, 1);
	}
	else if (c == -2147483648)
		write(1, "-2147483648", 11);
	else if (c < 0)
	{
		write(1, "-", 1);
		ft_putnbr(-c);
	}
	else
		write(1, &a, 1);
}
