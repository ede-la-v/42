#include "libft.h"

void	ft_putnbr_fd(int c, int fd)
{
	int a;

	a = c % 10 + 48;
	if (c >= 10)
	{
		ft_putnbr_fd(c / 10, fd);
		write(fd, &a, 1);
	}
	else if (c == -2147483648)
		write(fd, "-2147483648", 11);
	else if (c < 0)
	{
		write(fd, "-", 1);
		ft_putnbr_fd(-c, fd);
	}
	else
		write(fd, &a, 1);
}
