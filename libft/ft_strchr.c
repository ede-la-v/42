#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int i;

	i = 0;
	if (!s[0] && c == '\0')
		return ((char *)s);
	while (s[i])
	{
		if (s[i] == c)
		{
			return ((char *)&s[i]);
		}
		i++;
	}
	if (s[i] == c)
	{
		return ((char *)&s[i]);
	}
	return (NULL);
}
